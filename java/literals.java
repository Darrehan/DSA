// literals are contant values that are used in program .literals  have there own data type  
public class literals {

    public static void main(String args[]) {
        byte b1 = 10;
        byte b2 = 0b1010; // we can use diffrent number system
        byte b3 = 012;
        byte b4 = 0XA;

        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
        System.out.println(b4);

        long l = 9999999999L;
        float f = 12.56f;
        double d = 12.56d;

        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
    }

}